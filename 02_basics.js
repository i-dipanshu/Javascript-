// 3. Operators - used to perform an operation
// 3.a - Assignment Operator
console.log(2+3); // adds
console.log(2-3); // substracts
console.log(2*3); // multiply
console.log(2/3); // devides
console.log(2%3); // modulus
console.log(++number1); // increment
console.log(--number1); // decrement

// 3.b - Comparasion Operator - returns boolean value
console.log(2 == 3);  // checks the equality
console.log(2 === 3); // checks the equality and data types
console.log(2 != 3);  // not equal to
console.log(2 !== 3); // identical or not
console.log(2 < 3);   // less than
console.log(2 <= 3);   // less than or equal to
console.log(2 > 3);   // greater than 
console.log(2 >= 3);   // greater than

